// Палитра акцентов для рутин — чтобы карточки визуально не сливались.
// Цвет детерминирован по id, одинаковый у игрока и тренера.
const PALETTE = [
  '#F59E0B', // янтарный
  '#3B82F6', // синий
  '#8B5CF6', // фиолетовый
  '#14B8A6', // бирюзовый
  '#EC4899', // розовый
  '#84CC16', // лаймовый
  '#F97316', // оранжевый
  '#06B6D4', // голубой
];

export function routineAccent(id: number): string {
  return PALETTE[Math.abs(id) % PALETTE.length];
}
